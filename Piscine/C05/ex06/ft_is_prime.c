int	ft_is_prime(int nb)
{
	int i;
	int c;

	c = 1;
	i = 0;
	if (nb == 2)
		return (1);
	if (nb <= 1)
		return (0);
	while (i < nb / c)
	{
		i++;
		c++;
		if (nb % c == 0)
			return (0);
	}
	return (1);
}
