int	ft_iterative_factorial(int nb)
{
	int nbr;

	nbr = 1;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	while (nb > 0)
	{
		nbr *= nb--;
	}
	return (nbr);
}
